    private void updateUI(){
        int iAlm = ComboCentro.getSelectedIndex();
        assigModel.clear();
        if(iAlm>=0 && iAlm<centros.size()){
            assigModel.addAll(centros.get(iAlm).getMercancias().stream().map(m->m.toString()).toList());
        }
    }
    
    private void ComboCentroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboCentroActionPerformed
        updateUI();
    }//GEN-LAST:event_ComboCentroActionPerformed

    private void DelButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DelButActionPerformed
        int iCen = ComboCentro.getSelectedIndex(),iMer = AvailableList.getSelectedIndex();
        if(iCen>=0 && iCen<centros.size() && iMer>=0 && iMer<mercancias.size()){
            centros.get(iCen).getMercancias().remove(mercancias.get(iMer));
            try{
                aJPA.edit(centros.get(iCen));
                updateUI();
            }
            catch(Exception e){}
        }
    }//GEN-LAST:event_DelButActionPerformed

    private void AddButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButActionPerformed
        int iCen = ComboCentro.getSelectedIndex(),iMer = AvailableList.getSelectedIndex();
        if(iCen>=0 && iCen<centros.size() && iMer>=0 && iMer<mercancias.size()){
            centros.get(iCen).getMercancias().add(mercancias.get(iMer));
            try{
                aJPA.edit(centros.get(iCen));
                updateUI();
            }
            catch(Exception e){}
        }
    }//GEN-LAST:event_AddButActionPerformed

